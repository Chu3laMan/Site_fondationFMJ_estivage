/***************************************************************/
$(document).ready(function() {
  $(".anima1").hover(
    function() {
      $(".div-to-display1").addClass(" show-text");
      $(".div-to-display1").fadeIn();
    },
    function() {
      // out
      $(".div-to-display1").fadeOut();
      $(".div-to-display1").removeClass("show-text");
    }
  );
});
